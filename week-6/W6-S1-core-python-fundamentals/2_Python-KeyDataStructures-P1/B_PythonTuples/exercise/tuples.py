# tuple creation/initialization
this_tuple = (1, 3, 5, 7, "nine")
print(this_tuple)

# accessing first and third elements in tuple
print(this_tuple[0], this_tuple[2])

# using a for loop to iterate through the tuple and print each one
for item in this_tuple:
    print(item)
    
# tuple concatenation
another_tuple = ("cat", "dog")
new_tuple = this_tuple + another_tuple
print(new_tuple)

# tuple repetition
print(new_tuple * 2)

